public class Main {

	public static void main(String[] args) {
		 int x, y, z, m;

	        x=10; 
	        y=5;
	        z=16;
	        
	        m=++x - --y + z++;
	        
	        
	        
	        /*
	         * 11-4+16=23=m
	         * 
	         * 
	         * 
	         * 
	         */
	        System.out.println("z="+z);
	        System.out.println("x="+x);
	        System.out.println("y="+y);
	        System.out.println("m="+m);
	        
	}
}
